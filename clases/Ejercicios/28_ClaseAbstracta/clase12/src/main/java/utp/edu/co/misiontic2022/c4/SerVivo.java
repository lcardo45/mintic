public abstract class SerVivo {
    // Este metodo es generico y no se sabe como implementarlo y como no se
    // sabe como implementarlo es un metodo abstracto
    // El metodo abstracto es un metodo que va a estar en una superclase
    // Pero que tu no sabes como se implementa por que es muy generico
    // Cuando al menos se tiene un metodo abstracto la clase debera ser abstracta

    // Solo se define el metodo en la clase padre
    // En las clases hijas si se debe implementarse
    public abstract void alimentarse();

}
